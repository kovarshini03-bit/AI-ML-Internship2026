#Grade calculator
def calculator_grade(marks):

  if 90<= marks <=100:
    return "A"
  elif 80<= marks <=89:
    return "B"
  elif 70<= marks <=79:
    return "C"
  elif 60<= marks <=69:
    return "D"
  else:
    return "F"
marks=(int(input("Enter Marks")))
print("Grade:",calculator_grade(marks))

#Leap year checker
def is_leap_year(year):
  if year / 400==0:
    print("leap year")
  elif year / 100==0:
    print("not leap year")
  elif year / 4==0:
    print("leap year")
  else:
    print("not leap year")
year=(int(input("Enter a year")))
print(is_leap_year(year))

