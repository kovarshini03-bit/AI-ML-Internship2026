def sum_n(n):
  a=0
  for i in range(1,n+1):
    a+=i
  return a
n=int(input("enter n:"))
print("sum:",sum_n(n))

def factorial(n):
  a=1
  for i in range(1,n+1):
    a*=i
  return a
n=int(input("enter n:"))
print("factorial:",factorial(n))

